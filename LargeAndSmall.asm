	AREA PROG, CODE, READONLY
	ENTRY
	
SMALL RN R0
LARGE RN R1
NUM RN R3

	LDR R2 ,= ARRAY
	LDR SMALL, [R2], #4
	MOV LARGE, SMALL
	
LOOP
	
	LDR NUM, [R2], #4
	CMP NUM, #0
	BEQ DONE
	
	CMP NUM, SMALL
	MOVLO SMALL, NUM
	
	CMP NUM, LARGE
	MOVHI LARGE, NUM
	
	B LOOP

DONE
	B DONE

ARRAY DCD 7, 9, 23, 8, 0
	
	END